public class CriarArray {
    public static void main(String[] args) {
        String[] array1 = new String[]{"Oi, pessoal", "oioi"};
        String[] array2 = {"Oi, oi"};
        for(String i : array1) {
            System.out.println(i);
        }
        for(String i: array2) {
            System.out.println(i);
        }
    }
}
